export function Svg({ d }) {
  return (
    <svg viewBox="0 0 24 24" fill="currentColor" aria-hidden="true">
      {d}
    </svg>
  )
}

export function PageHeader({ title, subtitle, children }) {
  return (
    <div className="page-head">
      <div>
        <h1>{title}</h1>
        {subtitle && <p>{subtitle}</p>}
      </div>
      {children && <div className="head-actions">{children}</div>}
    </div>
  )
}

export function Kpi({ label, value, sub, accent = 'var(--primary2)', trend }) {
  return (
    <div className="kpi" style={{ '--accent': accent }}>
      <div className="k-label">{label}</div>
      <div className="k-value">{value}</div>
      <div className="k-sub">
        {trend !== undefined && trend !== null && (
          <span className={trend >= 0 ? 'up' : 'down'}>
            {trend >= 0 ? '▲' : '▼'} {Math.abs(trend * 100).toFixed(1)}%
          </span>
        )}
        {sub}
      </div>
    </div>
  )
}

export function ChartCard({ title, sub, children }) {
  return (
    <div className="chart-card">
      <h3>{title}</h3>
      {sub && <p className="ch-sub">{sub}</p>}
      <div className="chart-wrap">{children}</div>
    </div>
  )
}

export function Button({ icon, children, ...props }) {
  return (
    <button className="btn" {...props}>
      {icon && <Svg d={icon} />}
      {children}
    </button>
  )
}

export function EmptyState({ emoji = '📋', text }) {
  return (
    <div className="empty">
      <div className="big">{emoji}</div>
      {text}
    </div>
  )
}
